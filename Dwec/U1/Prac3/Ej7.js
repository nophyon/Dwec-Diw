var fecha = new Date();
var dia = fecha.getDay();

switch (dia){

    case 1:
      alert("Estamos a Lunes! \nQuedan 4 dias para el fin de semana")
      break;
    case 2:
      alert("Estamos a Martes! \nVenga que ya son solo 3 dias para el fin de semana")
      break;
    case 3:
      alert("Estamos a Miercoles! \nÁnimo que ya estamos a mitad de semana")
      break;
    case 4:
      alert("Estamos a Jueves! \nPor fin es Juernes universitario!")
      break;
    case 5:
      alert("Estamos a Viernes! \nEsta noche fiesta!")
      break;
    case 6:
      alert("Ya es Sábado!! \nA disfrutar del fin de semana")
      break;
    case 0:
      alert ("Hoy es domingo. \nTu ultimo dia de descanso!");
      break;






}
